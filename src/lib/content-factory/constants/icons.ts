export type IconName = keyof typeof ICON_MAP;

export function getIcon(name: IconName): LucideIcon {
  return ICON_MAP[name];
}

export function getIconSafe(name: string): LucideIcon {
  return ICON_MAP[name as IconName] || Zap;
}
