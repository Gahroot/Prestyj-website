function toTitleCase(value: string): string {
  return value
    .split("-")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");
}

function toPascalCase(value: string): string {
  return value
    .split(/[-\s]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

function escapeString(value: string): string {
  return JSON.stringify(value);
}

function list(items: readonly string[]): string {
  return items.map((item) => `      ${escapeString(item)},`).join("\n");
}

function lowerFirst(value: string): string {
  return value.charAt(0).toLowerCase() + value.slice(1);
}

function asAdjective(value: string): string {
  const lower = value.toLowerCase();

  if (lower.endsWith("s")) {
    return lower.slice(0, -1);
  }

  return lower;
}

function withArticle(value: string): string {
  if (value.toLowerCase().startsWith("ecommerce")) {
    return `an ${value}`;
  }

  const article = /^[aeiou]/i.test(value) ? "an" : "a";
  return `${article} ${value}`;
}

