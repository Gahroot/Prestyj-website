function ensureBlock(source: string, start: string, end: string, replacementBody: string): string {
  const startToken = `// ${start}`;
  const endToken = `// ${end}`;
  const block = `${startToken}\n${replacementBody}${replacementBody.endsWith("\n") ? "" : "\n"}${endToken}`;

  if (source.includes(startToken) && source.includes(endToken)) {
    const pattern = new RegExp(`${startToken}[\\s\\S]*?${endToken}`);
    return source.replace(pattern, block);
  }

  return source;
}

function patchBestForIndex(pages: GeneratedPage[]): void {
  const source = readFileSync(BEST_FOR_INDEX, "utf8");
  const imports = pages
    .map((page) => `import { ${page.variableName} } from "./${page.slug}";`)
    .join("\n");
  const registrations = pages
    .map((page) => `  "${page.slug}": ${page.variableName},`)
    .join("\n");

  let next = ensureBlock(
    source,
    "BATCH-ORGANIC-MATRIX-IMPORTS-START",
    "BATCH-ORGANIC-MATRIX-IMPORTS-END",
    `${imports}\n`,
  );
  next = ensureBlock(
    next,
    "BATCH-ORGANIC-MATRIX-REGISTER-START",
    "BATCH-ORGANIC-MATRIX-REGISTER-END",
    `${registrations}\n`,
  );

  const hasMarkers =
    source.includes("// BATCH-ORGANIC-MATRIX-IMPORTS-START") &&
    source.includes("// BATCH-ORGANIC-MATRIX-IMPORTS-END") &&
    source.includes("// BATCH-ORGANIC-MATRIX-REGISTER-START") &&
    source.includes("// BATCH-ORGANIC-MATRIX-REGISTER-END");

  if (!hasMarkers) {
    throw new Error(
      "Could not patch src/lib/best-for/index.ts. Add BATCH-ORGANIC-MATRIX marker blocks first.",
    );
  }

  writeFileSync(BEST_FOR_INDEX, next);
}

function main(): void {
  mkdirSync(BEST_FOR_DIR, { recursive: true });
  const pages = getPhaseOneBatchVideoAdsVerticals().flatMap((vertical) => [
    createBatchPage(vertical),
    createCreativeTestingPage(vertical),
  ]);

  for (const page of pages) {
    writeFileSync(join(BEST_FOR_DIR, `${page.slug}.ts`), page.content);
  }

  patchBestForIndex(pages);
  console.log(`Generated ${pages.length} organic batch video ad best-for pages.`);
}

main();
